		Felipe Sepúlveda, working under the pseudonym Alfacenttauri, creates luminous digital ecosystems that challenge our understanding of nature in an age of ecological crisis. His maximalist compositions blend polymorphic creatures into fantastical landscapes pulsing with saturated color and ethereal light, envisioning his work as a vast interconnected universe with its own internal logic and mythology.
	</p>
	<p>
		His practice emerges from a distinctly post-apocalyptic utopian vision, where destruction becomes generative rather than terminal, allowing new forms of beauty and existence to flourish. Through this lens, Alfacenttauri's radically imaginative worlds serve both as a critique of our current ecological and sociopolitical reality and as speculative portals into alternative ways of being.
