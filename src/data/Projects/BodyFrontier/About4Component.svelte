  <p>
    From intimate examinations of identity and beauty norms, Cyshimi and Brendy confront entrenched standards and personal experiences of judgment.
  </p>
  <p>
    Cyshimi draws on the history of nail art to reveal how this small part of the body embodies defiance and power, pushing at the boundaries between the artificial and the natural. Brendy channels São Paulo’s urban culture, upcycling practices, and experiences of bodily marginalization to create Monster Shoes—a fusion of cyborg aesthetics and deconstructed fashion that becomes sculptural, wearable protest.
  </p>
  <p>
    Both artists use digital space as a collaborative arena to imagine transmuted bodies, translating these virtual experiments into physical sculpture. Their work doesn’t just challenge conventional representations of the body; it confronts and redefines beauty, transformation, and political resistance.
  </p>
