  <p>
    Inspired by the periphery of São Paulo—alive with funk, hip-hop, skate, and street sports—Brendy reflects nostalgically on her experiences of lacking access to coveted items others owned.
    Confronting the fashion industry's lack of representation and adaptation for trans bodies and diverse identities, she was compelled to create her own pieces. She fuses childhood inspirations with upcycled materials to craft utopic wearable sculptures called Pisante, conceived as transmutable statements.
  </p>
  <p>
    Describing the act of creation itself as a performance, Brendy recorded Curação, a work in which she walks for two hours wearing two different sculptures, attempting to adapt to a new way of moving with these seemingly cyborg-like extensions of her body.
