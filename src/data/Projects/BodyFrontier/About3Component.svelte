  <p>
    Exploring themes of self-perception and identity in the realm of dreams and the subconscious, CymoonV investigates the possibilities of feminized and queer bodies.
  </p>
  <p>
    Her work focuses on constructing identity within virtual spaces. Through this practice, she seeks to move beyond conventional notions of embodiment, questioning ideas of body and self while inviting nature to act as an element of desire and connection.
  </p>
  <p>
    She aims to strip eroticism of its exclusively human perspective, presenting it instead as a universal, organic force—an insect penetrating a flower, a plant leaning toward the sun—where the impulse to reach or move toward something is an inherent constant.
