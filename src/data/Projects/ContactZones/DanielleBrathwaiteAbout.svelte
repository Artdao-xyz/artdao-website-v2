	<p>
		Gallery conventions dissolve in Brathwaite-Shirley’s practice, which treats installation space as
		total conversion. Terms and conditions appear first, planting choice physically—you have a body, a
		mind, an option. Walls become the game case, images and prints function as manuals, and custom
		controllers simplify experience, collapsing the gap between physical reference and digital
		interaction.
	</p>
	<p>
		The work happens not on screen or in the gallery, but in the choices audiences make. Bodies are
		fragmented—not archived physically but through thought, feeling, and action. Touch is central:
		<em>press to start, open to choose, shoot to protect</em>—your gestures set events in motion and
		return effort equally. Alone or with others, experience shifts; the awkward, silent, or failed
		moments become spaces to push connection and negotiation.
