	<p>
		When game worlds materialize as installations and avatars become tactile objects, haptics—the
		study and practice of touch as a mode of perception—generate a tension that resists resolution.
		Theo Triantafyllidis, Antigoni Tsagkaropoulou, and Danielle Brathwaite-Shirley refuse to treat
		physical space as an upgrade from the digital, forcing two realities to share a nervous system.
		Triantafyllidis collapses architecture, sound, and crowd behavior into misbehaving spatial
		games, where bodies negotiate trust, timing, and control as interfaces that misfire.
		Tsagkaropoulou uses materiality and scale to choreograph care and unease, making softness both
		political and relational. Brathwaite-Shirley plants choice physically in the gallery, turning
		gestures, attention, and inaction into the medium itself.
	</p>
	<p>
		Their work shows that haptic encounters matter less for bridging digital and physical than for
		revealing the systems we take for granted.
	</p>
