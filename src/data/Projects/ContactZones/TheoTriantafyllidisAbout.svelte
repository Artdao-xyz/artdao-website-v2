	<p>
		Game engines become spatial arts in Triantafyllidis’s hands, collapsing architecture, sound,
		lighting, and crowd behavior into misbehaving installations. Pastoral scenes—a hyper-muscled Ork
		resting amid golden hay and lute music—refuse payoff, letting presence and expectation collide.
		Viewers negotiate agency as muscle memory pushes them to act while the work insists on wandering,
		noticing, and improvising.
	</p>
	<p>
		In <em>Feral Metaverse</em>, language is stripped; survival requires gesture, timing, weight, and
		trust. Sculptural game stations force physical negotiation with others, where the room becomes a
		social system. Avatars—like Triantafyllidis’s Ork—misbehave too, exposing software’s assumptions
		about desire and power. The virtual mutates what needs to, the physical pulls in accountability:
		weight, constraint, shared control, and visible failure. Reality, he shows, is always constructed,
		negotiated, and embodied.
	</p>
