	<p>
		Materiality becomes political in Tsagkaropoulou’s work, refusing the binary between physical and
		digital. Fur, vinyl, and plush are not aesthetic—they resist neoliberal, techno-capitalist, and
		transphobic regimes of rigidity. Scale and tactility choreograph care and dependency, shifting who
		holds whom, whose body becomes environment for another.
	</p>
	<p>
		Softness produces queer relationality, calibrated with slight unease. The uncanny is not
		neutral—it emerges in which bodies and desires are legible as normal or deviant. Through
		post-cinematic softness, friction becomes a queer, ecologically sensitive mode of engaging
		technology, opening space for uncertainty, vulnerability, and relational experimentation without
		prescribing a single correct feeling.
	</p>
