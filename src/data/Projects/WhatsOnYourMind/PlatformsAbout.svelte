		The founding myth of social media promised a digital commons—diverse, customizable, and creatively free. A space to connect beyond the gatekeepers of traditional media. But the lived reality has proven far from utopian.
	</p>
	<p>
		Today, platforms—and the corporations behind them—are in the business of engagement. The Mark Zuckerbergs of the world understand creativity only in terms of screen time. Algorithms now reward what grabs attention: trending formats, commercial content, engineered virality. Artistic expression gets flattened into the ephemeral, leaving little room for reflection or public resonance.
	</p>
	<p>
		In response, artists are turning the system against itself. They study platform mechanics not to follow the rules, but to bend or break them. Algorithms become material—carved, glitched, spoofed. Posts double as traps, lures, and visibility experiments. The artist’s profile becomes a live site of inquiry: part persona, part archive, part tactical surface. These aren’t brands, but unstable circuits where emotion, attention, and resistance flicker and collide.
	</p>
