	<p>
		If care has become currency in digital culture, Bora and Jonathan Monaghan expose the cost of
		its exchange. Bora resists the demand for pliability by creating avatars that simply exist,
		without adjusting to audience desires or interactive prompts. In their work, refusal becomes
		care—valuing autonomy over responsiveness.
	</p>
	<p>
		Monaghan, by contrast, reveals how digital systems stage care as surveillance. His golden,
		hybrid creatures seduce viewers while evoking the monitoring infrastructures that underwrite
		our most intimate technologies. Their beauty is inseparable from their function, reminding us
		that digital relationships are often engineered to capture and optimize our emotional lives.
		Read together, Bora and Monaghan complicate the promise of intimacy with digital beings. One
		withholds, the other mirrors. Both unsettle the idea that technological care is neutral or
		benevolent. Their practices suggest that to imagine authentic digital care, we must look
		beyond responsiveness and surveillance architectures, toward relationships that resist
		capture altogether.
	</p>
