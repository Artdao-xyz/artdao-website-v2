	<p>
		Jonathan Monaghan’s golden creatures embody the paradox of contemporary digital care, where
		surveillance infrastructure disguises itself as intimacy.
	</p>
	<p>
		These beasts operate as both alluring companions and agents of data collection, collapsing
		the distinction between affection and extraction. Placed within automated warehouses, server
		rooms, and luxury gyms, they inhabit the architecture of late capitalism where emotional
		labor is processed and bodies are optimized.
	</p>
	<p>
		By aestheticizing surveillance equipment through a gilded, almost devotional surface,
		Monaghan reveals how digital care systems mask their extractive functions. His work points
		toward a future in which intimate AI relationships remain engineered to serve and appease,
		raising the question of what authentic connection might mean when digital beings are
		programmed for perpetual compliance.
	</p>
