	<p>
		PinkyBlue reframes nostalgia through a limited color palette inspired by early digital displays like the Game Boy. This restrained aesthetic highlights the algorithmic structure of her images and the creative problem-solving required at the pixel scale. Techniques like dithering—which simulate more colors through patterned interlacing—evoke the constraints of vintage digital systems.
	</p>
	<p>
		Here, pixel art is more than a style; it's a precise language for expressing complex ideas. Nostalgia becomes a tool to explore loss, longing, and fragmented memory—often through dreamlike or ambiguous scenes. While pixel aesthetics have reemerged in indie games, PinkyBlue doesn't aim to restore the past. She uses pixels as a contemporary tool.
	</p>
	<p>
		Rather than resist realism, her work proposes an alternative—valuing spontaneity, imperfection, and abstraction. It reintroduces friction, intimacy, and constraint into a digital culture defined by polish and precision.
	</p>
