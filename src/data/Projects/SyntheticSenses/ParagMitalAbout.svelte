	<p>
		Parag’s practice began with building systems that reflect perception back at itself: early sculptures
		tracked faces and projected them onto glass, and algorithms analyzed Daphne Oram's lost audio archive
		while giving archivists tools to perform with 60 hours of recovered material.
	</p>
	<p>
		This approach treats computational modeling as a way to interrogate gaps in theory, where neural
		networks built from neuroscience research reveal what’s missing in how we understand sensing itself.
	</p>
	<p>
		The Walt Disney Concert Hall project crystallized this approach at public scale. Twenty-eight terabytes
		of LA Philharmonic history—a hundred years of orchestral recordings, Björk performances, and Hollywood Bowl
		concerts—became performable through an immersive 3D browser. The work asked what thinking with archival
		sound looks like when anyone can code by talking to AI.
	</p>
	<p>
		Recent work pushes further into behavioral modeling. <em>Conflicts</em> uses AI dialogue to replay
		romantic arguments through metahuman avatars, creating digital mirrors that trauma prevents in life.
		<em>The Game of Whispers</em> simulates 20 characters spreading misinformation through Mughal politics, testing how lies propagate through
		language models. Conversational platforms let users jam with agents that program beats in real time. These
		aren’t tools for replacement but for augmentation, building what musicians need to think with massive amounts
		of data. Sound remains central even in visual work: vision tolerates noise, but audiences can’t sustain pure
		sonic chaos the same way.
	</p>
</div>
