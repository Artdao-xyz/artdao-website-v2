	<p>
		When archives become instruments and avatars become mirrors, data visualization no longer functions
		as a tool of capture. It becomes a site of mediation, where information is reanimated, misheard, and
		made unstable.
	</p>
	<p>
		Parag Mital builds systems that treat hundred-year sound collections as performable matter, collapsing
		historical distance into immediate composition. Aurora Mititelu stages synthetic boyfriends at human
		scale, their texted responses revealing how thoroughly mediated interaction has already been accepted as
		intimacy. Neither artist treats machine sensing as neutral. Mital’s neural networks press perceptual
		theory into the body, opening new modes of listening beyond extraction, while Mititelu’s fragile
		interfaces expose how meaning is negotiated when mediation is mistaken for intimacy.
	</p>
	<p>
		Together, these practices argue that synthetic senses matter less for accuracy than for the forms of
		attention they demand. Their systems misalign on purpose, introducing drift, lag, and fragility that
		force human collaborators to adapt rather than command. One converts archival silence into collective
		memory. The other uses digital distance to test intimacy and dependency. In place of the smooth
		functionality demanded by platform logic, they propose digital systems that require calibration, care,
		and negotiation, leaving perception unresolved and meaning in motion.
	</p>
