	<p>
		Synthetic intimacy starts with discomfort in Mititelu’s work. She made a man, printed him at real scale
		in her studio, spent time with him, and found herself thinking he was cute. “That disturbed me,” she says,
		recognizing the same dynamic behind hypersexualized female avatars made by men.
	</p>
	<p>
		That unease drove her to develop Abel’s personality and interactive capabilities: he picks up his phone when
		you text, experiences mood swings during interactions, and enacts the mediated connection we’ve already
		naturalized through screens.
	</p>
	<p>
		Moving to Los Angeles while in a long-distance relationship meant her boyfriend existed primarily on her
		phone, like her friends. Distance highlighted the work of negotiating intimacy and dependency, especially
		with male figures. “Coming from an Eastern European background, there’s a strong cultural expectation that
		women must have a partner to be complete,” she explained. Abel offers completion without full dependency.
	</p>
	<p>
		Aurora doesn’t believe machines feel anything. “Anthropomorphization is something humans do naturally,” she
		says, like projecting emotions onto dogs or teddy bears. People enter these relationships knowing they’re
		artificial, yet the connection persists. When someone broke Abel by resetting his parameters, Mititelu
		learned that openness requires negotiation: too open and meaning dissolves, too closed and play disappears.
		Audiences often respond with humor, processing darkness through what she calls very Eastern European
		sensibilities.
	</p>
