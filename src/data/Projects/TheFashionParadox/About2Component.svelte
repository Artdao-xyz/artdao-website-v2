	<p>
		Matheus Araújo confronts the tension between digital design and physical production in fashion, using 3D modeling tools to test what garments can do beyond the body’s conventional limits. With these tools, he deliberately slows his process, prioritizing thoughtful craftsmanship over the rapid pace of digital consumption.
	</p>
	<p>
		Through his brand, Museu Gráfico Visual, Araújo transforms these ideas into tangible, wearable art. Working from his São Paulo studio, he combines meticulous pattern-making, screen printing, and careful material selection to produce garments that function as both canvas and statement. His pieces circulate in Brazilian street culture, allowing wearers to embody art while making fashion a space where digital possibility and physical reality coexist, collide, and redefine.
	</p>
