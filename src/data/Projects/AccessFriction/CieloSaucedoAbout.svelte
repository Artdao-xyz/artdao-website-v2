	<p>
		Technology becomes reclamation in Cielo G. Saucedo’s practice, claiming space within labor lineages that
		disabled people have long occupied but rarely controlled. Digital infrastructures often rely on Mexican
		labor while excluding Mexican workers from authorship. As a Mexican and disabled artist, Cielo positions
		their work within this history—not as beneficiary, but as maker and laborer—claiming the tools rather than
		inheriting them.
	</p>
	<p>
		Recent work takes the form of a game installation that refuses VR as a medium while exposing it as symbol.
		Sixteen-channel spatialized sound allows visitors to experience the work even without playing. Access
		expands, but friction remains. Players don headsets within the game, triggering optical shifts that force
		them to navigate border zones as robots disarming sentry towers. VR blinds, beheads, extracts. It grants
		able bodies temporary otherness while sanitizing the surveillance deployed at actual borders.
	</p>
