	<p>
		Digital space offered Nat access, a way to make work while the body rests, escaping the physical demands of
		a studio. Virtual environments hold social and embodied potential often dismissed as secondary to “real”
		presence. Weightlessness, suspended physics, and alternative movement reorganize embodiment rather than
		escape it. Virtual experience becomes genuine encounter, intimacy, becoming.
	</p>
	<p>
		<em>Cripping CG</em> emerged from friction with tools designed to exclude. The data stays messy—no cleaning,
		no standardization. Each asset is bespoke, shaped through consent with the person whose movement it
		captures. When institutional equipment access ended, the project adapted to free browser tools, encountering
		new biases and new glitches. The work continues not because technology improved, but because tools don’t
		dictate terms. Sensors move to mobility devices, extending embodiment beyond the human form. The archive
		insists disability is presence: gradated, material, irreducible. The mess is the point.
	</p>
