		Motion capture systems require standardized poses, and VR headsets assume upright, able bodies. Disabled
		artists expose how these platforms encode exclusion directly into their design. Cielo G. Saucedo and Natalee
		Decker refuse to treat inaccessible technology as a fixable oversight, collapsing the gap between
		exclusion and architecture. Their project <em>Cripping CG</em> confronts consumer motion capture that only
		calibrates for normative bodies. The data stays messy—jagged spasms, limbs at odd angles, bodies flickering
		between positions become aesthetic and political material rather than errors to correct.
