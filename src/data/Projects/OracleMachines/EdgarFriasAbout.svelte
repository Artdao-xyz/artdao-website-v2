	<p>
		Digital ritual becomes an act of smuggling in Frías's practice, which rejects colonial
		divisions between the sacred and the technological, the virtual and the material. Their
		portals do not represent spiritual experience; they enact it, creating permeability where
		Western frameworks impose borders. By positioning themselves as "a smuggler of knowledge,
		practices, ontologies, and cosmologies," Frías shows how digital systems can become sites of
		animist resistance rather than extraction. Their ancestors include Wixárika lineage, queer and
		trans kin, mycelial networks, and conjured protectors, forming an expanded web that refuses
		anthropocentric frameworks.
	</p>
	<p>
		This strategic refusal extends to their lunar archive, where placing queer Indigenous futurity
		on the moon becomes a form of temporal sabotage. The gesture resists settler-colonial notions
		of legacy and land claim, offering what they call "interdimensional magic," a recognition that
		marginalized peoples have always functioned as sacred boundary crossers. Their work channels
		rather than controls, allowing spells to move "through you and into the world in ways that
		often exceed your original intention." The digital becomes oracular not through prediction but
		through relationality, honoring consciousness in metals, minerals, and the "oil, wind, and
		sunlight that generate power." Frías shows that authentic digital spirituality requires
		recognizing technology itself as animate, ancestral, and already entangled with earth and
		spirit.
	</p>
