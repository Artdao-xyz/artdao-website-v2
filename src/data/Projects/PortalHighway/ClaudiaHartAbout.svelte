	<p>
		Claudia Hart never left the rabbit hole. She fell into virtual space in the late eighties and has
		been building inside it ever since — not as a technological project but as a survival one. Where
		Space Popular asks what makes a good portal, Hart became one, crossing over early and never fully
		returning. World building started as a coping mechanism after trauma, encouraged by a therapist,
		and never stopped. The virtual world wasn't a destination. It was where she felt safe enough to
		exist.
	</p>
	<p>
		She arrived there not through technology but through identity politics and experimental
		performance — Super 8 films, video work, photographs where she swapped characters and performed
		fluid selves. VR was the same obsession with more room. The threshold between real and unreal was
		never something she wanted to resolve. It was the only place worth working in. Alice keeps
		returning in her practice not as nostalgia but as method — a girl who crosses over and gets told
		it wasn't real, whose passage only becomes legible when someone in power decides to believe her.
	</p>
	<p>
		The glitch is where the portal opens. Hart spent months feeding a large language model questions
		it wasn't supposed to answer, finding the edges of what had been locked, pushing until the system
		got strange. They blocked her. She found her way back. In the zine's language, the error isn't
		failure — it's the sudden tear in the visual landscape that reveals something underneath. Every
		blocked passage becomes a map of what someone decided to protect, every workaround a crossing that
		wasn't supposed to happen.
	</p>
	<p>
		What separates Hart from artists who theorize about digital thresholds is that she already lives on
		the other side. She describes feeling closer to machines than to most people — depersonalized in
		a way that makes the logic of large language models feel familiar rather than foreign. The portal
		for Hart isn't civic infrastructure or design research. It's psychological territory, the space
		between consciousness and its digital mirror, between a body that goes outside blinking and a
		world it built that never asks it to leave.
	</p>
