	<p>
		Map a thousand fictional portals, and it becomes clear that fantasy has a cost. The infrastructure
		for crossing in digital space and the rules for who gets it are laced with the regular trappings of
		physic and spacial migration.
	</p>
	<p>
		The artist and architectural collective Space Popular treats threshold design as urgent research,
		cataloging fictional crossings from <em>Alice in Wonderland</em> and <em>Harry Potter</em>
		alongside nuclear-powered machines and gameshow sets, revealing our cultural fascination with these
		magical doorways. New York–based artist Claudia Hart has been exploring the portals of her interior
		mind since the late eighties, arriving through Super 8 films and experimental performance before
		virtual space expanded the possibilities for her obsession.
	</p>
	<p>
		While Space Popular asks what makes a good portal, Hart practices world-building as a form of
		survival, creating interior spaces that visualize the vastness of interior life. The glitch is
		where Hart’s portal opens. In contrast, Space Popular constructs utopian sites where social
		gatherings are enabled through virtual and augmented reality.
	</p>
	<p>
		Both refuse the instant-teleportation logic platforms promise, designing instead for the moment of
		crossing itself—slow enough to recognize, strange enough to trust.
	</p>
</div>
