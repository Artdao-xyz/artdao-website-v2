	<p>
		Salawaki is a digital artist working across 3D, NFTs, and internet culture. Based on Siquijor Island, her practice draws on CGI, blockchain-native platforms, and online vernacular to construct a form of digital folklore shaped by the ambient noise of networked life. Humor and meme logic operate as formal tools, allowing confusion, emotional excess, and self-irony to remain active rather than resolved.
	</p>
	<p>
		Across her video-based and 3D works, Salawaki appears through a recurring digital avatar, often accompanied by a nearly ever-present trout. This figure functions as both anchor and interference, accumulating meaning through obsessive return and minor variation. Rather than illustrating an inner world, the avatar sustains it, enabling a practice built through drift, repetition, and attention.
	</p>
	<p>
		What emerges is a visual language where performance, mediation, and introspection collapse into one another, and where tenderness and breakdown coexist without hierarchy.
	</p>
