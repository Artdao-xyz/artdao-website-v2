	<p>
		Artist Huntrezz Janos approaches digital sustainability through an off-grid practice that is
		both a survival strategy and a creative constraint. Working with solar-powered digital art,
		they expose the material realities beneath supposedly immaterial spaces, where energy
		budgeting becomes a methodology rather than a limitation.
	</p>
	<p>
		Through TRANSFER Data Trust, Huntrezz demonstrates how individual technological autonomy
		depends on collective infrastructure. Their approach to resource management—batching heavy
		jobs for sunny hours, choosing compression over brute-force processing—directly challenges
		the myth of infinite digital resources embedded in corporate platform design.
	</p>
	<p>
		A recurring theme in their work is digital decay as a form of garden maintenance, drawing
		parallels between technological systems and natural cycles. This embrace of impermanence
		operates as preparation rather than pessimism, embedding resilience and sustainability into
		even the most temporary digital forms.
	</p>
