	<p>
		Feed AI enough human text, and it starts to sound like everything at once. Instead, some artists are building languages that these machines will never know.
	</p>
	<p>
		Alice Bucknell treats the limits of legibility as an aesthetic and political position. The language they construct for their Mars project isn't designed to communicate with the audience. It's designed to exist without them. A communication system for a place that doesn't need our permission to be real. Space lawyers, NASA researchers, and linguistic anthropologists give them the foundation to push off from into somewhere genuinely strange.
	</p>
	<p>
		Kyle McDonald treats the limits of AI systems as the most interesting thing about them. Where researchers use neural networks to track endangered whales, McDonald uses the same tools to expose the assumptions buried inside them. The misuse isn't destructive. It's diagnostic. When a system fails, it shows you what it was really built for and who it was built to serve.
	</p>
	<p>
		Together, they suggest that the abundance of AI knowledge is most interesting at its borders. One builds languages for places AI can't reach. The other builds games out of the places AI gets wrong. Both treat creation as something that begins precisely where knowledge ends.
	</p>
