    Drawn to memes as a consumer, Julian recognized their cultural significance in shaping how we communicate, convey ideologies, and participate in digital communities. At the same time, he noticed art’s diminishing ability to address controversial issues, while memes offered an accessible and irreverent way to confront such topics.
  </p>
  <p>
    Fascinated by memes that were enigmatic or stripped of context, Julian saw clear parallels with conceptual art. These observations gradually merged with his practice, which had been rooted in academic and traditional art forms.
  </p>
  <p>
    This convergence of ideas and research led to a collaboration with Frenetik Void and the creation of "Psipsikoko"—a surreal, AI-generated world shaped by meme culture and surrealist logic.
