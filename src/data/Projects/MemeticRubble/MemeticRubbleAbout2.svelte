  <p>
    Interested in appropriating and deconstructing existing memes, Femzor discovered in this visual vocabulary something far more potent than humorous images. Memes became a form of digital communication capable of delivering sharp political commentary, challenging norms and institutions, and functioning as tools of resistance.
  </p>
  <p>
    These images circulate rapidly across digital communities in ways that feel distinctly human. They amplify critical perspectives that might otherwise go unheard beyond the platforms where they emerge.
  </p>
  <p>
    Rejecting the expectations placed on artists within traditional art worlds, Femzor uses meme culture as an act of protest. By incorporating this language into his work, he critiques the very institutions he seeks to disrupt.
