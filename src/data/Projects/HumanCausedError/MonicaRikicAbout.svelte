	<p>
		Mónica Rikić's practice begins from a different point of entry, but arrives at a similar
		dismantling of technological narratives. Working with DIY electronics, robotics, and interactive
		systems, she treats technology not as an autonomous force, but as a human construct shaped by
		social, economic, and political conditions. Her <em>Feeding Robot</em> project exemplifies this
		approach. Developed within a research context focused on assistive robotics, the work initially
		aimed to engage with questions of usability and acceptance. However, the project quickly shifted
		toward a broader critique of the systems in which such technologies operate, particularly the
		neoliberal logic that frames care as a problem to be solved through automation.
	</p>
	<p>
		Rather than producing a functional, optimized device, Rikić constructs a robot that is visibly
		handmade, awkward, and prone to failure. Its aesthetic resists the polished invisibility of
		commercial technologies, foregrounding instead the labor, decisions, and assumptions embedded in
		its construction. This refusal of seamlessness is both aesthetic and political. A perfect machine
		obscures the conditions that produce it, the labor it replaces, the infrastructures it depends on,
		and the bodies it excludes. By contrast, a malfunctioning or imperfect device makes these
		conditions visible, opening space for critique and reinterpretation.
	</p>
	<p>
		At the core of this work is a redefinition of care. Rikić positions care not as a discrete task that
		can be automated, but as an ecology, an entanglement of relationships, institutions, and
		constraints. Technologies designed to assist often reduce this complexity, framing care as a series
		of actions that can be optimized or outsourced. Her work resists this reduction. By introducing
		friction into the system through failure, hesitation, and non-functionality, she reasserts the
		role of agency. The user is no longer a passive recipient of technological assistance, but an
		active participant in negotiating how care is given and received. In this context, the question is
		not whether technology can replace human action, but whether it can accommodate the complexity of
		human needs. The answer, her work suggests, lies not in efficiency, but in the ability to remain
		unresolved.
	</p>
