	<p>
		Mastery of the human body and its environment remains the dominant narrative of technological
		progress. Devices are designed to appear stable, predictable, and universally applicable, masking
		the complex infrastructures and assumptions that sustain them. When systems fail, when a robot
		hesitates, a sensor glitches, or a body refuses calibration, this illusion collapses. Friction
		exposes the limits of design. It reveals that technologies are not neutral tools, but are embedded
		with cultural, economic, and ideological decisions about how bodies should behave and what forms of
		interaction are considered valid.
	</p>
	<p>
		In this context, error becomes more than a technical issue. It becomes a form of counter-knowledge
		production. Artists working with technological systems increasingly refuse the imperative to
		correct or conceal malfunction. Instead, they amplify it. They build practices around instability,
		treating unpredictability as both an aesthetic strategy and a critical method. What emerges is not a
		rejection of technology, but a reconfiguration of its role from a system of control to a space of
		negotiation.
	</p>
</div>
