	<p>
		Marco Donnarumma's work operates at the threshold between control and loss of control, where both
		machines and bodies begin to exceed their intended functions. By engineering his own devices,
		biosensors, robotic prostheses, and sound systems, he gains intimate access to the logic of
		technological systems, allowing him to push them beyond their prescribed limits. Central to his
		practice is a shift away from prosthesis as correction. Rather than designing devices that fix or
		extend the individual body, Marco reconceives prosthetics as distributed organs, technologies that
		connect multiple bodies into shared systems of sensation.
	</p>
	<p>
		In this framework, embodiment is no longer contained within a single subject but becomes
		relational, unstable, and collective. This is particularly evident in works that amplify internal
		bodily signals. Unlike external data, movement tracked by accelerometers or cameras, internal
		signals are inherently noisy. Muscle contractions, bone vibrations, and blood flow produce data that
		is inconsistent, context-dependent, and impossible to fully control. These signals are shaped by
		emotion, environment, and physiological state, making them resistant to standardization. Rather
		than filtering this noise, Marco centers it. The instability of the data becomes the work itself.
		It demands a continuous negotiation between performer and system, where control is partial and
		constantly shifting.
	</p>
	<p>
		In his more recent projects, this instability extends into shared experience. Wearable devices
		transmit internal signals between bodies, allowing participants to feel each other's rhythms,
		heartbeats, movements, and vibrations within their own. These encounters are intimate but
		disorienting, collapsing boundaries between self and other while foregrounding the technological
		mediation that enables them. What emerges is not a seamless fusion of human and machine, but a
		fragile system of dependencies. Technology does not stabilize the body. It amplifies its
		unpredictability.
	</p>
