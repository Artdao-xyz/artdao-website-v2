		Virk approaches digital presence as both a political act and a survival strategy. Her early
		adoption of the masculine-coded alias “virk” exposed the default assumptions embedded in
		online spaces. Over time, her shift toward greater visibility came from recognizing the
		importance of visibility in male-dominated ecosystems.
	</p>
	<p>
		Her experience as a Latin American artist illustrates how global digital platforms often
		amplify colonial gazes. The fetishization she encountered marked her identity as something to
		be consumed—directly confronting the commodification of culture in virtual space. A recurring
		strategy in her work is the use of cute aesthetics as resistance, drawing on Sara Ahmed’s
		theory of affective circuits: elements socially linked to tenderness or innocence can move and
		unsettle us as powerfully as an act of violence. This strategic deployment of cuteness
		operates as camouflage, transmitting complex messages about power, identity, and resistance
		through seemingly innocuous forms.
