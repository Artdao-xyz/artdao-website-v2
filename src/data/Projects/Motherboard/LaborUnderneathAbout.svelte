	<p>
		Invisible labor becomes material when it is rearticulated through deliberate gestures and objects—no longer
		hidden, but foregrounded as something to witness, enter, and engage with.
	</p>
	<p>
		Wednesday Kim treats the invisible as something to curate—a show that gathers it, names it, and creates space
		for it to be experienced without smoothing it over. <em>The Rise of the Care Machines</em> doesn’t explain
		motherhood from a distance; it comes from inside the work itself: scanning your body mid-feed, constructing a
		virtual city alongside a growing pregnancy, answering a child’s question about existence with
		<em>we are data</em>. The exhibition becomes civic infrastructure for a community that barely existed in
		digital art spaces until someone insisted it be seen.
	</p>
	<p>
		Addie Wagenknecht treats the invisible as something to expose, not through confession but through formal
		precision. The Roomba doesn’t explain feminist theory—it moves across her body, leaving a record of what it
		cannot map. The robotic arm doesn’t argue for policy—it rocks, and the rocking is the argument. Both artists
		refuse the language of complaint, replacing it with systems, objects, and machines that make the labor legible
		and tangible.
	</p>
	<p>
		Together, their work shows that care and maintenance aren’t metaphors—they are the same structure running on
		different hardware. One maps the community, the other maps the body. Both refuse to let the work disappear.
	</p>
</div>
