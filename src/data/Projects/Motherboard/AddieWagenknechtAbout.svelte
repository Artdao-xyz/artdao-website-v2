	<p>
		Addie Wagenknecht’s work began with observation and experimentation: noticing dust respond to movement,
		thinking about scale, Action Painting, and what it would mean to make work interactive without human control.
		That curiosity evolved into experiments with robotic arms, Roombas, and other machines, tools through which she
		could explore labor, repetition, and presence. When she became pregnant at Carnegie Mellon University, her focus
		sharpened: what happens when a machine takes on care work that is normally invisible, repetitive, and unending?
	</p>
	<p>
		This thinking produced <em>The Optimization of Parenting</em>, a robotic arm programmed to respond to a crying
		baby. The arm mediates the mother’s labor rather than erasing it, making repetitive care visible. In later work,
		Wagenknecht placed herself nude on a canvas while a Roomba navigated her body, leaving traces of what it could
		not map. The marks document absence and presence, reflecting the erasure of women’s labor, availability, and care
		in domestic and technological systems. Across her projects, Wagenknecht foregrounds repetition, maintenance, and
		labor as tangible and political, showing how care—whether human or machine-mediated—structures both everyday life
		and digital systems.
	</p>
