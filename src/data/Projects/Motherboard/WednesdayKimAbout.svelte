	<p>
		Wednesday Kim makes the invisible labor of mothers visible. She began by posting dark, honest memes about
		motherhood—the kind that show what it feels like when no one is watching. Other mothers responded, asking
		<em>“Are you a mother too?”</em> That recognition became a community, and that community became the foundation
		for her exhibition, <em>The Rise of the Care Machines</em>. The show began online and later expanded into
		galleries, creating a platform for digital artists who are also mothers—a community largely absent from
		digital art discourse.
	</p>
	<p>
		The exhibition refuses to sentimentalize motherhood, instead curating works that make care legible: scanning a
		body mid-feed, constructing virtual cities alongside pregnancy, and anonymizing intimate labor with AI-generated
		voices. Artists explore the constant attention, maintenance, and repetition that sustain human and
		technological systems. Mollye Bendell’s lenticular loops depict a headless breastfeeding figure in motion,
		simultaneously soothing the baby and trapping the mother. Faith Holland maps skincare routines directly onto
		device screens, collapsing bodily and technological maintenance into a single gesture. Snow Yunxue Fu built a
		virtual city during her pregnancy, a parallel life unfolding alongside her own. The exhibition itself becomes
		civic infrastructure, showing how care circulates across bodies, machines, and communities. In Kim’s work,
		technology and care are inseparable: both require ongoing attention, both fail when that attention lapses, and
		both produce meaning in the errors as well as the finished work.
	</p>
